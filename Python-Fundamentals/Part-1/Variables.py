# A variable is used to store data in memory.
# Here, 'name' is an identifier that stores a string value.
name = "Prince"

# print() is a built-in function used to display output on the screen.
print(name)

# 'age' is another variable storing an integer value.
age = 20

# Multiple values can be printed together using commas.
# Python automatically adds space between them.
print(name, age)

# Strings and variables can be combined in a print statement.
# Commas are used instead of + to avoid type conversion issues.
print("My name is", name, "and I am", age, "years old")


# ---------------- IDENTIFIERS ----------------
# Identifiers are the names given to variables, functions, classes, etc.
# Examples: name, age, print

# Rules for identifiers:
# 1. They can contain letters, digits, and underscores (_)
# 2. They must not start with a digit
# 3. They must not be Python keywords
# 4. They are case-sensitive (age and Age are different)

# ---------------- CASE SENSITIVE ----------------
# Python is case-sensitive.
# Example: 'name' and 'Name' are treated as different identifiers.

